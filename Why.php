	<p>Why Study Here
The raison d'etre of EU is its great people: our graduates, students, faculty and management. Since the commencement of its journey in 1994, EU has progressed by leaps and bounds both as an institution and the programs it delivers to the students. The focus here is to impart quality education and to train the students to become leaders of tomorrow in their chosen fields. A university, it is said, makes a solemn pledge to uphold high standards and inculcate the same in its students, so that when they graduate we expect them to gain recognition and acclaim in the academia, society and industry. It is with pride and gratitude that we acknowledge the recognition our alumni have earned in the pursuit of their professional lives. Working with partners and stakeholders from different sectors both at home and abroad, EU has gained prominence in both academic programs and management services. International accreditation of many of its programs now enables the university's students to attain admissions at many leading international academic institutions for higher studies. The ISO Certification 9001-2008 with a scope on "Quality Management Operation System for the University" is another evidence of quality. The Job Placement of the graduates is enhanced due to AIUB's reputation.   AIUB has also made its mark in the field of sports and its students regularly bring home laurels from different tournaments. The medium of instruction at AIUB is English. All class rooms are fully air conditioned and well equipped with multimedia facilities. The University has a well stocked library, on campus cafeteria, a gymnasium and many other facilities to make the tenure of our students a truly enjoyable experience. AIUB's milestone achievements have been made possible by the able stewardship of dedicated and visionary founders, dynamic management, competent faculty and skillful support staff.</p>


