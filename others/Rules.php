<h2 >Rules of Entry to EU Campus Premise </h2></div><br><br><br>

<p>



	1.NO ID; NO ENTRY policy is strictly applicable for the Students and Employees of the university.<br>
2.Students and Employees must wear and display their ID at all times inside the campus.<br>
3.Student ID at the automated flap/ swing doors is only granted for VALID registered student of the current semester.<br>
4.Unregistered/ INVALID students or alumni carrying Student ID must use the entry in front of the Registrar’s office and identify their current status at the validation kiosk. They will be directed to the respective concerned offices based on their current status.<br>
5.Every individual regardless of student/ faculty members/ alumni, entering through the entry in front of Registrar’s office must tap their ID at the validation kiosk.<br>
6.In case the ID of a valid registered student or an employee is not functional, they must be verified at the entry point in front of Registrar’s office. After verification, they may proceed to the VUES Office to solve their ID problem as per university policy.<br>
7.Visitors or Guardians are allowed to enter the campus only for valid and verified reasons. They must submit a valid Photo ID and collect official ‘Visitor ID’. The ‘Visitor ID’ must be displayed at all times during their stay in the campus. Visitor ID Must be returned and deposited Photo ID must be collected at the end of the visit while leaving the campus premise.<br>
8.Visitors of an Employee or Guardians willing to meet faculty members or officials, will be allowed to enter only if their visit is confirmed and agreed by the person they are willing to meet.<br>
9.Gates marked ‘No Entry’ are strictly used for ‘Exit only’; i.e. North Gate of Annex 1, Side gate near the automated flap doors between Annex 2 and 3, collapsible gate between Annex 4 and 5, and Exit gate in front of Registrar office.<br>
10.Security personnel may use their master card to open the automated flap doors only for employees in case of malfunctioning ID.<br>
11.EU reserves all the rights to allow or disallow anyone from entering the premise.<br>
